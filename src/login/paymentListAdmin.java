    /**
     * @param args the command line arguments
     */
        private void fetchPaymentData() {
        Connection dbconn = DBConnection.connectDB();
            try (PreparedStatement st = dbconn.prepareStatement("SELECT * FROM balance");
                 ResultSet resultSet = st.executeQuery()) {

                while (resultSet.next()) {
                    int id = resultSet.getInt("id");
                    String name = resultSet.getString("name");
                    String amount = resultSet.getString("amount");
                    String month = resultSet.getString("expiration");
                    
                    DefaultTableModel model = (DefaultTableModel)tblPaymentLogs.getModel();
                    model.addRow(new Object[]{id, name, amount, month});
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
}
